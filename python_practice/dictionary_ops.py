#Dictionary (Mutable)
a1 = {} #empty dictionary
a1["1"] = 10 
a1["2"] = 20
print(a1)

#Useful functions
a1.keys() #returns keys of the dictionary
a1.values() #returns values of dictionary
a1.items() #returns all items of dictionary

#Tuple as a key
matrix = {(0,2):1, (2,4):9}
print(matrix)





